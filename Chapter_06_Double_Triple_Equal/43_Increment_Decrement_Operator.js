//Decrement
let a = 10;
let b = --a; // (decrement the a by 1)

//Pre decrement -> --a -> decrease the value and then assign
console.log(b);  //9
console.log(a);  //9

let a = 10;
let b = a--; // decrement the a by 1

//Post decrement -> a-- -> Assign the value and then decrease the value
console.log(b);  //10
console.log(a);  //9